/**
 * **Basic data types**
 */
const myNumber = 3 // This is a number
const myOtherNumber = 34.98 // This is a other number
const myString = 'Hello world' // This is a string
const myBoolean = true // This is a boolean
console.log('Examples of basics data types: ', myNumber, myOtherNumber, myString, myBoolean)
/* * * * * * * * * * * * * * * * * * */
/**
 * **Arithmetic Operators**
 * Arithmetic operators are used to perform arithmetic between variables and/or values.
 * + Addition
 * - Subtraction
 * * Multiplication
 * / Division
 * % Modulus (division remainder)
 * ++ Increment
 * -- Decrement
 */
/* * * * * * * * * * * * * * * * * * */
/**
 * **Assignment Operators**
 * Assignment operators are used to assign values to JavaScript variables.
 * =  -> x = 5
 * += -> x += 5
 * -= -> x -= 5
 * *= -> x *= 5
 * /= -> x /= 5
 * %= -> x %= 5
 */
/* * * * * * * * * * * * * * * * * * */
/**
 * **String Operators**
 * The + operator, and the += operator can also be used to concatenate (add) strings.
 * +  -> text3 = text1 + text2
 * += -> text1 += text2
 */
/* * * * * * * * * * * * * * * * * * */
/**
 * **Comparison Operators**
 * Comparison operators are used in logical statements to determine equality
 * or difference between variables or values.
 * ==  -> equal to
 * === -> equal value and equal type
 * !=  -> not equal
 * !== -> not equal value or not equal type
 * >   -> greater than
 * <   -> less than
 * >=  -> greater than or equal to
 * <=  -> less than or equal to
 */
/* * * * * * * * * * * * * * * * * * */
/**
 * **Conditional (Ternary) Operator**
 * The conditional operator assigns a value to a variable based on a condition.
 * variable-name = (condition) ? value1: value2
 * voteable = (age < 18) ? "Too young": "Old enough"
 *
 * Example explained: If the  variable "age" is a value below 18,
 * the value of the  variable "voteable" will be "Too young",
 * otherwise the value of voteable will be "Old enough".
 */
/* * * * * * * * * * * * * * * * * * */
/**
 * **Logical Operators**
 * Logical operators are used to determine the logic between variables or values.
 * && -> and
 * || -> or
 * !  -> not
 */
